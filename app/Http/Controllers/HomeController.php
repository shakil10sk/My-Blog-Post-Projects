    }



    public function singleview($id){
        $view=DB::table('posts')
        ->join('categories','posts.category_id','categories.id')
        ->select('posts.*','categories.slug','categories.name')
        ->where('posts.id',$id)
        ->first();
        return view('home.singleView',compact('view'));
    }

